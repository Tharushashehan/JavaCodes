// Relational Operator Demo
public class Test11 {
public static void main(String args[])
{
int a = 1,b = 8;
System.out.println("a is " + (a));
System.out.println("a << 2 is " + (a << 2));
System.out.println("a is " + (a));
System.out.println("b >> 1 is " + (b >> 1));
System.out.println("b is " + (b));
System.out.println("a++ << 2 is " + (a++ << 2));
System.out.println("a is " + (a));
System.out.println("++a << 2 is " + (++a << 2));
System.out.println("a is " + (a));
System.out.println("b-- >> 2 is " + (b-- >> 2));
System.out.println("b is " + (b));
System.out.println("--b >> 2 is " + (--b >> 2));
System.out.println("b is " + (b));
System.out.println("b >>> 1 is " + (b >>> 1));
System.out.println("b is " + (b));
}
}